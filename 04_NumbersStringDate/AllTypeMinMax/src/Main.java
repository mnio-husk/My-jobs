public class Main {
    public static void main(String[] args) {
        //Number:whole -> byte
        System.out.print("Byte:максимум " + Byte.MAX_VALUE + ", минимум ");
        System.out.println(Byte.MIN_VALUE + "; \n");

        //Number:whole -> int
        System.out.print("Integer:максимум " + Integer.MAX_VALUE + ", минимум ");
        System.out.println(Integer.MIN_VALUE + "; \n");

        //Number:whole -> short
        System.out.print("Short:максимум " + Short.MAX_VALUE + ", минимум ");
        System.out.println(Short.MIN_VALUE + "; \n");

        //Number:whole -> long
        System.out.print("Long:максимум " + Long.MAX_VALUE + ", минимум ");
        System.out.println(Long.MIN_VALUE + "; \n");

        //=========================================================

        //Number:float point -> float
        System.out.print("Float:максимум " + Float.MAX_VALUE + ", минимум ");
        System.out.println(Float.MIN_VALUE + "; \n");

        //Number:float point -> float
        System.out.print("Double:максимум " + Double.MAX_VALUE + ", минимум ");
        System.out.println(Double.MIN_VALUE + "; \n");
    }
}
